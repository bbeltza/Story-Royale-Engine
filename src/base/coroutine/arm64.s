#include "armcommon.c"

__attribute__((naked)) static void sys_coroutineswitch(coroutine_native* coroutine, coroutine_native* current)
{
    __asm(
        "STR LR, [X1, #0*8]\n\t"
        "STR X0, [X1, #2*8]\n\t"
        "STR X1, [X1, #3*8]\n\t"
        "STR X2, [X1, #4*8]\n\t"
        "STR X3, [X1, #5*8]\n\t"
        "STR X4, [X1, #6*8]\n\t"
        "STR X5, [X1, #7*8]\n\t"
        "STR X6, [X1, #8*8]\n\t"
        "STR X7, [X1, #9*8]\n\t"
        "STR X8, [X1, #10*8]\n\t"
        "STR X9, [X1, #11*8]\n\t"
        "STR X10, [X1, #12*8]\n\t"
        "STR X11, [X1, #13*8]\n\t"
        "STR X12, [X1, #14*8]\n\t"
        "STR X13, [X1, #15*8]\n\t"
        "STR X14, [X1, #16*8]\n\t"
        "STR X15, [X1, #17*8]\n\t"
        "STR X16, [X1, #18*8]\n\t"
        "STR X17, [X1, #19*8]\n\t"
        "STR X18, [X1, #20*8]\n\t"
        "STR X19, [X1, #21*8]\n\t"
        "STR X20, [X1, #22*8]\n\t"
        "STR X21, [X1, #23*8]\n\t"
        "STR X22, [X1, #24*8]\n\t"
        "STR X23, [X1, #25*8]\n\t"
        "STR X24, [X1, #26*8]\n\t"
        "STR X25, [X1, #27*8]\n\t"
        "STR X26, [X1, #28*8]\n\t"
        "STR X27, [X1, #29*8]\n\t"
        "STR X28, [X1, #30*8]\n\t"
        "STR X29, [X1, #31*8]\n\t"

        // SP cannot be stored directly
        "MOV X2, SP\n\t"
        "STR X2, [X1, #1*8]\n\t"

        // Context switch!
        "LDR LR, [X0, #0*8]\n\t"
        "LDR X1, [X0, #3*8]\n\t"
        "LDR X3, [X0, #5*8]\n\t"
        "LDR X4, [X0, #6*8]\n\t"
        "LDR X5, [X0, #7*8]\n\t"
        "LDR X6, [X0, #8*8]\n\t"
        "LDR X7, [X0, #9*8]\n\t"
        "LDR X8, [X0, #10*8]\n\t"
        "LDR X9, [X0, #11*8]\n\t"
        "LDR X10, [X0, #12*8]\n\t"
        "LDR X11, [X0, #13*8]\n\t"
        "LDR X12, [X0, #14*8]\n\t"
        "LDR X13, [X0, #15*8]\n\t"
        "LDR X14, [X0, #16*8]\n\t"
        "LDR X15, [X0, #17*8]\n\t"
        "LDR X16, [X0, #18*8]\n\t"
        "LDR X17, [X0, #19*8]\n\t"
        "LDR X18, [X0, #20*8]\n\t"
        "LDR X19, [X0, #21*8]\n\t"
        "LDR X20, [X0, #22*8]\n\t"
        "LDR X21, [X0, #23*8]\n\t"
        "LDR X22, [X0, #24*8]\n\t"
        "LDR X23, [X0, #25*8]\n\t"
        "LDR X24, [X0, #26*8]\n\t"
        "LDR X25, [X0, #27*8]\n\t"
        "LDR X26, [X0, #28*8]\n\t"
        "LDR X27, [X0, #29*8]\n\t"
        "LDR X28, [X0, #30*8]\n\t"
        "LDR X29, [X0, #31*8]\n\t"

        "LDR X2, [X0, #4*1]\n\t"
        "MOV SP, X2\n\t"

        "LDR X2, [X0, #4*8]\n\t"
        "LDR X0, [X0, #2*8]\n\t"

        "RET"
    );
}